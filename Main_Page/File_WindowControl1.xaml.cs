    public sealed partial class File_WindowControl1 : UserControl
    {
        public File_WindowControl1()
        {
            this.InitializeComponent();
        }

        private async void File_Button_Click(object sender, RoutedEventArgs e)
        {
            var folderPicker = new Windows.Storage.Pickers.FolderPicker();
            folderPicker.SuggestedStartLocation = Windows.Storage.Pickers.PickerLocationId.Desktop;
            folderPicker.FileTypeFilter.Add("*");

            Windows.Storage.StorageFolder folder =await folderPicker.PickSingleFolderAsync();
            if (folder != null)
            {
                // Application now has read/write access to all contents in the picked folder
                // (including other sub-folder contents)
                Windows.Storage.AccessCache.StorageApplicationPermissions.
                FutureAccessList.AddOrReplace("PickedFolderToken", folder);
                File_Textbox.Text=  folder.Path;
            }
            else
            {
                File_Textbox.Text = "Operation cancelled.";
            }
        }

